    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        // TODO add your handling code here:
        
        if(Validar.isEmpty(txt_nome))
        {
            JOptionPane.showMessageDialog(null, "Campo [nome do curso] está vázio, porfavor preecha!");
        }
        
        else if(Validar.isEmpty(txt_curso))
        {
            JOptionPane.showMessageDialog(null, "Campo [nome do curso] está vázio, porfavor preecha!");
        }
        else{
            Aluno a = new Aluno();
            //dados do aluno.
            long numero_matricula  = (long) spin_matricula.getValue();
            
            if(dao.verificarId((long)this.spin_matricula.getValue()))
            {
                JOptionPane.showMessageDialog(null, "Esse número de matricula já consta no sistema!");
            }
            else if(Validar.isNotLetters(txt_curso) == false)
            {
                JOptionPane.showMessageDialog(null, "Nome de curso inválido, tente novamente");
            }
            else if(Validar.isNotLetters(txt_nome) == false)
            {
                JOptionPane.showMessageDialog(null, "Nome de aluno inválido, tente novamente");
            }
            else{
                a.setMatricula(numero_matricula);
                a.setNome(txt_nome.getText());
                a.setCurso(txt_curso.getText());
                a.setNota1((float)spin_nota1.getValue());
                a.setNota2((float)spin_nota2.getValue());
                a.setNota3((float)spin_nota3.getValue());
                a.setNota4((float)spin_nota4.getValue());
                dao.inserir(a);
                modelo.atualizarListaTabela(dao.findAll());
                JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso...");
            }
            
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

