    private void btn_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removerActionPerformed
        // TODO add your handling code here:
        Object[] opcoes =  {"Não", "Sim"};
        int resultado = JOptionPane.showOptionDialog(null, "Deseja deletar esse registro?", "Aviso",JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, opcoes, opcoes[1]);
        
        switch(resultado)
        {
            case 1:
                int posicao = this.tbl.getSelectedRow();
                if(posicao == -1)
                {
                    JOptionPane.showMessageDialog(null,"É necessário selecionar uma linha, para remover!");
                }
                else{
                    Aluno a = new Aluno();
                    a.setMatricula((long) this.tbl.getValueAt(this.tbl.getSelectedRow(), 0));
                    this.modelo.removerLinhaDaTabela(posicao);
                    this.dao.remover(a);
                    JOptionPane.showMessageDialog(null,"Removido com sucesso...");
                    this.modelo.atualizarListaTabela(this.dao.findAll());
                }
                
            break;
            
        }
    }//GEN-LAST:event_btn_removerActionPerformed

    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        // TODO add your handling code here:
       
        if(dao.verificarId((long)this.spin_buscar.getValue()) == false)
        {
            JOptionPane.showMessageDialog(this, "Matricula não registrada!");
            modelo.atualizarListaTabela(dao.findAll());
        }
        else{
            modelo.limparTabela();
            Aluno a = new Aluno();
            a = dao.getById((long)spin_buscar.getValue());
            modelo.adicionarNovoElementoNaLinhaDaTabela(a);
            JOptionPane.showMessageDialog(this, "Matricula Encontrada!");
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2)
        {
            Aluno a = new Aluno();
            a.setMatricula((long)this.tbl.getValueAt(this.tbl.getSelectedRow(), 0));
            a.setNome((String)this.tbl.getValueAt(this.tbl.getSelectedRow(), 1));
            a.setCurso((String)this.tbl.getValueAt(this.tbl.getSelectedRow(),2));
            a.setNota1((float) this.tbl.getValueAt(this.tbl.getSelectedRow(), 3));
            a.setNota2((float) this.tbl.getValueAt(this.tbl.getSelectedRow(), 4));
            a.setNota3((float) this.tbl.getValueAt(this.tbl.getSelectedRow(), 5));
            a.setNota4((float) this.tbl.getValueAt(this.tbl.getSelectedRow(), 6));
            Editar editar = new Editar();
            editar.exportarDados(a);
            editar.setVisible(true);
            this.setDefaultCloseOperation(this.EXIT_ON_CLOSE);
            this.dispose();
            
        }
    }//GEN-LAST:event_tblMouseClicked

